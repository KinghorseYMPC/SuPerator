"""Generate methodology.pdf for SuPerator submissions.

Priority: fpdf2 if available, else raw PDF bytes (dependency-free minimal PDF).
No API key, no config.yaml, no LLM, no remote calls.
Output writes to outputs/submission/submission/ (git-ignored).
"""
from __future__ import annotations

from pathlib import Path
from typing import Any

ROOT = Path(__file__).resolve().parents[2]

# ---------------------------------------------------------------------------
# Unicode → ASCII sanitization
# ---------------------------------------------------------------------------

_UNICODE_REPLACEMENTS: dict[int, str] = {
    0x2014: "-",   # — (em dash)
    0x2013: "-",   # – (en dash)
    0x2018: "'",   # ' (left single quote)
    0x2019: "'",   # ' (right single quote)
    0x201C: '"',   # " (left double quote)
    0x201D: '"',   # " (right double quote)
    0x2192: "->",  # → (right arrow)
    0x00D7: "x",   # × (multiplication)
    0x2264: "<=",  # ≤
    0x2265: ">=",  # ≥
    0x2026: "...", # …
    0x00B7: "*",   # · (middle dot)
    0x2212: "-",   # − (minus sign)
    0x00A0: " ",   # non-breaking space
}


def sanitize_pdf_text(text: str) -> str:
    """Replace Unicode characters unsupported by PDF built-in fonts with ASCII.

    Helvetica/Courier fonts only support WinAnsiEncoding (cp1252 subset).
    Characters outside this range cause fpdf2 to raise an error.
    """
    result: list[str] = []
    for ch in text:
        cp = ord(ch)
        if cp in _UNICODE_REPLACEMENTS:
            result.append(_UNICODE_REPLACEMENTS[cp])
        elif cp < 128:
            result.append(ch)
        elif 160 <= cp <= 255:
            # cp1252 upper range — may or may not be in the font
            # Keep them if printable
            result.append(ch)
        else:
            # Drop characters outside the safe range
            result.append("?")
    return "".join(result)


